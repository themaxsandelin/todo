// User clicked on the add button
// If there is any text inside the item field, add that text to the todo list
document.getElementById('add').addEventListener('click', function() {
  var value = document.getElementById('item').value;
  if (value) {
    addItem(value);
  }
});

document.getElementById('item').addEventListener('keydown', function (e) {
  var value = this.value;
  if ((e.code === 'Enter' || e.code === 'NumpadEnter') && value) {
    addItem(value);
  }
});

function addItem (value) {
  addItemToDOM(value);
  document.getElementById('item').value = '';

  data.todo.push(value);
  dataObjectUpdated();
}

function renderTodoList() {
  if (!data.todo.length && !data.completed.length) return;

  for (var i = 0; i < data.todo.length; i++) {
    var value = data.todo[i];
    addItemToDOM(value);
  }

  for (var j = 0; j < data.completed.length; j++) {
    var value = data.completed[j];
    addItemToDOM(value, true);
  }
}

function dataObjectUpdated() {
  localStorage.setItem('todoList', JSON.stringify(data));
}

function removeItem() {
  var item = this.parentNode.parentNode;
  var parent = item.parentNode;
  var id = parent.id;
  var value = item.innerText;

  if (id === 'todo') {
    data.todo.splice(data.todo.indexOf(value), 1);
  } else {
    data.completed.splice(data.completed.indexOf(value), 1);
  }
  dataObjectUpdated();

  parent.removeChild(item);
}

function completeItem() {
  var item = this.parentNode.parentNode;
  var parent = item.parentNode;
  var id = parent.id;
  var value = item.innerText;

  if (id === 'todo') {
    data.todo.splice(data.todo.indexOf(value), 1);
    data.completed.push(value);
  } else {
    data.completed.splice(data.completed.indexOf(value), 1);
    data.todo.push(value);
  }
  dataObjectUpdated();

  // Check if the item should be added to the completed list or to re-added to the todo list
  var target = (id === 'todo') ? document.getElementById('completed'):document.getElementById('todo');

  parent.removeChild(item);
  target.insertBefore(item, target.childNodes[0]);
}

function editItem() {
    var item = this.parentNode.parentNode;
    if (this.className == 'input__edit-confirm') {
        item = this.parentNode;
    }

    if (!item.querySelector('.input__edit')) {
        item.originValue = item.innerText;
        item.firstChild.textContent = '';
        item.querySelector('.buttons').style.display = 'none';

        var editInput = document.createElement('input');
        editInput.classList.add('input__edit');
        editInput.type = 'text';
        editInput.value = item.originValue;
        item.insertBefore(editInput, item.firstChild);

        var inputConfirm = document.createElement('button');
        inputConfirm.innerText = 'Ok';
        inputConfirm.classList.add('input__edit-confirm');
        inputConfirm.addEventListener('click', editItem);
        item.appendChild(inputConfirm);
    } else {
        editedValue = item.querySelector('.input__edit').value;

        item.querySelector('.input__edit').remove();
        item.querySelector('.input__edit-confirm').remove();
        item.querySelector('.buttons').style.display = '';
        item.firstChild.textContent = editedValue;

        var parent = item.parentNode;
        var id = parent.id;
        if (item.originValue != editedValue) {
            if (id === 'todo') {
                idNum = data.todo.indexOf(item.originValue);
                data.todo[idNum] = editedValue;
            } else {
                idNum = data.completed.indexOf(item.originValue);
                data.completed[idNum] = editedValue;
            }
            dataObjectUpdated();
        }
    }
}

// Adds a new item to the todo list
function addItemToDOM(text, completed) {
  var list = (completed) ? document.getElementById('completed'):document.getElementById('todo');

  var item = document.createElement('li');
  item.innerText = text;

  var buttons = document.createElement('div');
  buttons.classList.add('buttons');

  var remove = document.createElement('button');
  remove.classList.add('remove');
  remove.innerHTML = removeSVG;

  // Add click event for removing the item
  remove.addEventListener('click', removeItem);

  var complete = document.createElement('button');
  complete.classList.add('complete');
  complete.innerHTML = completeSVG;

  // Add click event for completing the item
  complete.addEventListener('click', completeItem);

  var edit = document.createElement('button');
  edit.classList.add('edit');
  edit.innerHTML = editSVG;

  // Add click event for completing the item
  edit.addEventListener('click', editItem);

  buttons.appendChild(remove);
  buttons.appendChild(complete);
  buttons.appendChild(edit);
  item.appendChild(buttons);

  list.insertBefore(item, list.childNodes[0]);
}
